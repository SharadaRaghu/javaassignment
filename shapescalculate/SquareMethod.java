 package com.shapescalculate;

public class SquareMethod {
	 public void area()
	 {
		 System.out.println("null param");
	 }
	
		public int area( int i)
		{
			return (i*i);
		}
		public double area( double b)
		{
			return (b*b*b);
		}
		public float area(float x, float y)
		{
			return (x+y);
		}

	}

	
	


